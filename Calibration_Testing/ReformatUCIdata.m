Plant.Data.Timestamp = (datenum([2010,1,1,0,15,0]):1/96:datenum([2014,1,1,0,0,0]))';
Plant.Data.Holidays = [];
n = length(Plant.Data.Timestamp);
Plant.Data.Temperature = zeros(n,1);
Plant.Data.Demand.E = zeros(n,1);
Plant.Data.Demand.H = zeros(n,1);
Plant.Data.Demand.C = zeros(n,1);
index1 = 1;
for y = 2010:1:2013
    year = strcat('h',num2str(y));
    nS = length(Data.(year).TEShot);
    index2 = index1+nS-1;
    Plant.Data.Temperature(index1:index2,1) = 5/9*(Data.(year).WetBulbTemp(1:nS)'-32);
    Plant.Data.Demand.E(index1:index2,1) = 1000*Data.(year).DemandE(1:nS)';
    Plant.Data.Demand.C(index1:index2,1) = 1000*Data.(year).DemandC(1:nS)';
    Plant.Data.Demand.H(index1:index2,1) = 1000*Data.(year).DemandH(1:nS)';
    index1 = index2+1;
    Data.(year).DryBulbTemp = Data.(year).DryBulbTemp(1:nS)';
    Data.(year).WetBulbTemp = Data.(year).WetBulbTemp(1:nS)';
    Data.(year).RelativeHumidity = Data.(year).RelativeHumidity(1:nS)';
    Data.(year).DemandE = Data.(year).DemandE(1:nS)';
    Data.(year).DemandC = Data.(year).DemandC(1:nS)';
    Data.(year).DemandH = Data.(year).DemandH(1:nS)';
    Data.(year).Import = Data.(year).Import(1:nS)';
    Data.(year).GT = Data.(year).GT(1:nS)';
    Data.(year).ST = Data.(year).ST(1:nS)';
    Data.(year).Solar = Data.(year).Solar(1:nS)';
    Data.(year).SteamGen = Data.(year).SteamGen(1:nS)';
end
